#include <iostream>
using namespace std;
int main() {
    int a,b,c;
    cin>>a>>b>>c;
    if(a>=3&&b>=3&&c>=3) {
        cout << (a-2)*(b-2)*(c-2) << " "
            << (a-2)*(b-2)*2+(a-2)*(c-2)*2+(b-2)*(c-2)*2 << " "
            << (a-2)*4+(b-2)*4+(c-2)*4 << " "
            << 8 << " "
            << 0 << " "
            << 0 << " "
            << 0 << " ";
    }else {
        if(a>b) swap(a,b);
        if(a>c) swap(a,c);
        if(b>c) swap(b,c);
        if(a==1) {
            if(b==1) {
                if(c==1) {
                    cout<<0<<" "<<0<<" "<<0<<" "<<0<<" "<<0<<" "<<0<<" "<<1;
                } else {
                    cout<<0<<" "<<0<<" "<<0<<" "<<0<<" "<<c-2<<" "<<2<<" "<<0;
                }
            } else {
                cout<<0<<" "<<0<<" "<<(c-2)*(b-2)<<" "<<(b-2)*2+(c-2)*2<<" "<<4<<" "<<0<<" "<<0;
            }
        } else {
            cout<<0<<" "<<(c-2)*(b-2)*2<<" "<<(b-2)*4+(c-2)*4<<" "<<8<<" "<<0<<" "<<0<<" "<<0;
        }
    }
}
